    /*
     *@desc This function saves a codes list to an external file. It passes through a HashMap paraemeter
     * and uses it to write the list into the file. 
     */
    public void saveFile(HashMap<Integer, String> list) throws IOException {
        // Asks user if they want to save file
        int optionPane = JOptionPane.showConfirmDialog(null, "Do you want to save this list for future use?");
	File file = new File("svl.txt"); // Calls a new file svl (saved lists)
	file.createNewFile(); // File is created
	PrintWriter p = new PrintWriter(new FileWriter(file)); // PrintWriter to print list to file
        switch (optionPane) { // Switch-case block to see which option was selected
            case JOptionPane.YES_OPTION: // Yes was selected
                for(Entry<Integer, String> entry: list.entrySet()) { // Loops through an Entry set. Entry sets are easier to use since we can view the keys AND the values
                    p.println(entry.getKey() +","+ entry.getValue()); // Prints the list in comma-delimited format
                    p.flush(); // Flushes the last line (if there are any extra empty lines left)
                }
                p.close(); // Closes PrintWriter
                JOptionPane.showMessageDialog(null, "List saved"); // Tells user that list is saved
                break;
            case JOptionPane.NO_OPTION: // If No is selected
                // Lets user know that list will not be saved
                JOptionPane.showMessageDialog(null, "List will not be saved");
                break;
            default: // Cancel button
                // Lets user know that list will not be saved
                JOptionPane.showMessageDialog(null, "List will not be saved");
                break;
        }
    }
    
