    private void nextBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextBtnActionPerformed
        CardLayout card = (CardLayout)mainPanel.getLayout(); // Creates CardLayout object for mainPanel
        // If-statement to check which button was selected
        if(setupList.isSelected()){ // If setupList button is selected
            // Warns user that a new list will be set up
            int option = JOptionPane.showConfirmDialog(null, "Any previous lists will be discarded to make a new list. Do you accept?", "Warning", JOptionPane.INFORMATION_MESSAGE);
            if(option == 0){ // If option is yes
                resultTxt.setText(null); // Clears the result field
                codes.clear(); // Clears codes for more input
                card.show(mainPanel, "createList"); // Shows createList panel
            }else if(option == 1){ // If option is no
                // Prompts user to make different selection
                JOptionPane.showMessageDialog(null, "Please select a different option if you do not want to discard old list.");
            }
        }else if(importList.isSelected()){ // If importList is selected
            result1Txt.setText(null); // Empties result1Txt text field
            card.show(mainPanel, "importCodes"); // Shows importList panel
            // Shows confirm dialog 
            int option = JOptionPane.showConfirmDialog(null, "The file should be a .txt file with item codes in the first column and item names in second column.\nDo you want to proceed?");
            if(option == 0){
                JFileChooser jd = new JFileChooser(); // Creates object JFileChooser for user to choose a file to import
                jd.setDialogTitle("Choose input file"); // Title of FileChooser object
                int returnVal= jd.showOpenDialog(null); // Return value is needed for file selection
                if (returnVal != JFileChooser.APPROVE_OPTION); // Verifies that the returnVal isn't null
                    Path s = jd.getSelectedFile().toPath(); // Creates a path which tracks from where the file is found
                    String dir = s.toString(); // This path is converted to a string in order to put in a scanner
                    Scanner fh = new Scanner(System.in); // New scanner is made
                    String fileName = dir; // fileName is the path of the imported file
                try { // try-catch block for scanning file
                    fh = new Scanner(new File(fileName)); // File is put in Scanner
                        while(fh.hasNextLine()) { // Scanning file
                            String line = fh.nextLine(); // Stores each line in String line
                            String[] data = line.split(","); // Line is split by their commas and put into temporary array
                            if(data.length == 2) { // If array has two elements
                                try{
                                    codes.put(Integer.parseInt(data[0]), String.valueOf(data[1])); // Elements are added to codes HashMap
                                    result1Txt.setText(result1Txt.getText() + data[0] + "-" + data[1] + "\n"); // Displays list in text area
                                }catch(NumberFormatException e){ // If the values are not correct
                                    // Error message is shown to user
                                    JOptionPane.showMessageDialog(null, "Values inputted are invalid.", "Error", JOptionPane.ERROR_MESSAGE, null);
                                }  
                            }
                        }
                    // Indicates to user that the list has been added
                    JOptionPane.showMessageDialog(null, "The list has been added.");
                    saveFile(codes); // Calls saveFile function
                }catch(FileNotFoundException i) {
                    // Shows error message to user
                    JOptionPane.showMessageDialog(null, "File not found");
                }catch(IOException e1){
                    // Shows error message to user
                    JOptionPane.showMessageDialog(null, "IO Exception, Restart Program");
                }
            }else if(option == 1){
                card.show(mainPanel, "setupPage1");
            }
        }else if(editList.isSelected()){ // If editList is selected
            String fileName = "svl.txt"; // This is the file that will be searched for a saved list
            try {
                Scanner fh = new Scanner(new File(fileName)); // Scanner to scan file
                    while(fh.hasNextLine()) { // Loop to scan file
                        String line = fh.nextLine(); // String line takes in each line of file
			String[] data = line.split(","); // Line gets split and inputted into an array
			if(data.length == 2) { 
                            // Array is added to list codes
                            codes.put(Integer.parseInt(data[0]), String.valueOf(data[1]));
			}
                    }
                    JOptionPane.showMessageDialog(null, "List found"); // Tells user list is found
            }catch(FileNotFoundException i) {
                // Shows message to user if an exception occurs
		JOptionPane.showMessageDialog(null, "No save file found. Make sure to save the list before editing it.");
                card.show(mainPanel, "setupPage1"); // Redirects user to setup page
            }
            if(codes.isEmpty()) { // If code list is empty
                // Gives error to the user
                JOptionPane.showMessageDialog(null, "List is empty, please import or make a new one.");
		card.show(mainPanel, "setupPage1"); // Redirects user to setup page
            }else { // If list is not empty
            card.show(mainPanel, "editCodes"); // Directs user to editing panel
            }
        }else if(continueList.isSelected()){ // If continueList is selected
            card.show(mainPanel, "continueOld"); // Shows continueList panel
            Scanner fh = new Scanner(System.in); // Creates scanner
            String fileName; // Creates String for file name
            fileName = "svl.txt"; // "svl.txt" should be the default where the program saves the list
            try{ //try-catch block for Scanner
                fh = new Scanner(new File(fileName)); // Scans file
                while(fh.hasNextLine()){ // Loops through file
                    String line = fh.nextLine(); // String to store each line
                    String[] data = line.split(","); // Splits line by commas and adds it to temporary array
                    if(data.length == 2){ // Checks if there are 2 elements in data array
                        codes.put(Integer.parseInt(data[0]), String.valueOf(data[1])); // Adds the elements to HashMap
                        result2Txt.setText(result2Txt.getText() + data[0] + "-" + data[1] + "\n"); // Displays list in text area
                    }
                }
                // Lets user know that list is added
		JOptionPane.showMessageDialog(null, "The list has been added.");
            }catch(FileNotFoundException i){
                // Shows error message to user
                JOptionPane.showMessageDialog(null, "File not found");
            }
            if(codes.isEmpty()){ // If HashMap is empty
                // Show error message to user
                JOptionPane.showMessageDialog(null, "File is empty, please import new file", "Empty list", JOptionPane.ERROR_MESSAGE, null);
		card.show(mainPanel, "setupPage1");
            }else{
		
            }
        }else{ // If none of the buttons are selected
            // System gives user an error message 
            JOptionPane.showMessageDialog(null, "Please select an option.", "Select an option", JOptionPane.ERROR_MESSAGE, null);
        }
    }//GEN-LAST:event_nextBtnActionPerformed

