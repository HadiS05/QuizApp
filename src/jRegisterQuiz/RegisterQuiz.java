    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        try {
            int guess = Integer.parseInt(answerTxt.getText());
            if (guess == correct && count <= 3) { // If guess is correct and the count is less than or equal to 3
                JOptionPane.showMessageDialog(null, "Correct!"); // Tell user they are correct
                wins++; // Increase wins
                answerTxt.setText(null); // Empties text field
                count = 1; // Resets count
                game(codes);
            } else if (guess != correct && count >= 3) { // If answer is not correct and more than 3 turns are taken
                JOptionPane.showMessageDialog(null, "The correct answer was: " + correct); // Tell user the correct answer
                answerTxt.setText(null); // Empties text field
                count = 1; // Resets count
                game(codes);
            } else if (guess != correct && count < 3) {
                JOptionPane.showMessageDialog(null, "Try again");
                answerTxt.setText(null); // Empties text field
                count++; // Increases count 
            }
        } catch (NumberFormatException ie) { // Invalid input
            // Shows error message to user
            JOptionPane.showMessageDialog(null, "Invalid code.", "Error", JOptionPane.ERROR_MESSAGE, null);
            answerTxt.setText(null); // Empties text field
        }
    }//GEN-LAST:event_submitBtnActionPerformed

