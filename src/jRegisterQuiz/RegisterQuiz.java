    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try{ // try-catch block to make sure user inputs correct fields
            String name = itemNameTxt.getText(); // Gets item name 
            int code = Integer.parseInt(itemCodeTxt.getText()); // Gets item code
            if(codes.containsKey(code)){ // If statement to check if user has inputted duplicates
                // Shows error message to user
                JOptionPane.showMessageDialog(null, "Duplicate code found, please use a different code.", "Duplicate", JOptionPane.ERROR_MESSAGE, null);
                itemCodeTxt.setText(null); // Empties the field
                return; // Ends the continuation of the program
            }else if(codes.containsValue(name)){
                // Shows error message to user
                JOptionPane.showMessageDialog(null, "Duplicate name found, please use a different name.", "Duplicate", JOptionPane.ERROR_MESSAGE, null);
                itemNameTxt.setText(null); // Empties the field
                return; // Ends the continuation of the program
            }
            codes.put(code, name); // Adds code and name to the HashMap
            itemNameTxt.setText(null); // Resets text field for name
            itemCodeTxt.setText(null); // Resets text field for item code
            resultTxt.setText(resultTxt.getText() + code + "-" + name + "\n"); // Displays the HashMap in resultTxt
        }catch(NumberFormatException e){ // If wrong input is added
            // Shows error to user
            JOptionPane.showMessageDialog(null, "Please make sure that valid values are added.", "Error", JOptionPane.ERROR_MESSAGE, null);
            itemNameTxt.setText(null); // Resets text field for name
            itemCodeTxt.setText(null); // Resets text field for item code
        }
    }//GEN-LAST:event_addBtnActionPerformed

